package bucles;

public class Bucle {
    /**
     * Este main todo imprime una cuenta regresiva de 1 a 10.
     *
     * @param args los argumentos que se pasan al ejecutar el main todo
     */
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("Contando: " + (i+1));
        }
    }
}
